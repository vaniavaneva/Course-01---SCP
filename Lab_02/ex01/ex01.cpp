#include <iostream>
#include <stdio.h>

int main()
{
    int binary1 = 0b1111111;
    int binary2 = 0b11001110;
    int binary3 = 0b1010110;
    int binary4 = 0b10000110000000;
    printf("1111111  (2) -> %d  (10)\n", binary1);
    printf("11001110 (2) -> %d  (10)\n", binary2);
    printf("1010110  (2) -> %d  (10)\n", binary3);
    printf("10000110000000  (2) -> %d  (10)\n", binary4);
}
